package com.java.miniproject.pojo;

public class Hobby {

	private int id;
	private int User_Id;
	private String Hobby;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUser_Id() {
		return User_Id;
	}
	public void setUser_Id(int hobby_Id) {
		User_Id = hobby_Id;
	}
	public String getHobby() {
		return Hobby;
	}
	public void setHobby(String hobby) {
		Hobby = hobby;
	}
	
	
	
}
